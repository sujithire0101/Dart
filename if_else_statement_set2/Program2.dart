



//Write a dart program to print the colour of rainbow based on input

void main(){

	var x=7;

	switch(x){

		case 1:
		print("Violet");

		case 2:
		print("Indigo");
		
		case 3:
		print("Green");
		
		case 4:
		print("Blue");

		
		case 5:
		print("Yellow");
		
		case 6:
		print("Orange");
		
		case 7:
		print("Red");
		
		default:
		print("Invalid Input");
	}

}
